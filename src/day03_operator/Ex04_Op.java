package day03_operator;

public class Ex04_Op {
	public static void main(String[] args) {
		/*
		 true(참), false(거짓)
		 a = 10, b = 20;
		 a > b : false		왼쪽이 크다
		 a <= b : true		오른쪽 크거나 같다
		 a == b : false		비교 하는것 
		 a != b : true		같지 않다
		 */
		double do1 = 3.0, do2 = 3.1;
		System.out.println(do1 > do2);
		System.out.println(do1 <= do2);
		System.out.println(do1 == do2);
		System.out.println(do1 != do2);
		
	}
}
